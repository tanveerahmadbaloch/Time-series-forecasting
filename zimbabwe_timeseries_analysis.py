def calculate_mase(actual_series, forecast_series, seasonal_period):
    """
    Calculate Mean Absolute Scaled Error (MASE)
    """
    n = len(actual_series)
    d = np.abs(np.diff(actual_series)).sum() / (n - 1)

    errors = np.abs(actual_series - forecast_series)
    mase = errors.mean() / d
    return mase

