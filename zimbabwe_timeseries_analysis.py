# Define a function to fit ARIMA and forecast for the given series
def fit_and_forecast(series, forecast_periods):
    model = ARIMA(series, order=(1, 0, 0))
    model_fit = model.fit()
    forecast = model_fit.forecast(steps=forecast_periods)
    return forecast

